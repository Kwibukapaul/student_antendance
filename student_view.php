            <?php
// Database connection
try {
    $host = 'localhost';
    $dbname = 'student_attendance';
    $username = 'root';
    $password = '';

    // Establishing a connection to the database using PDO
    $pdo = new PDO("mysql:host=$host;dbname=$dbname", $username, $password);

    // Setting PDO in exception mode
    $pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);

    // SQL query to retrieve data from the student table
    $sql = "SELECT * FROM student";
    $stmt = $pdo->query($sql);

    if ($stmt->rowCount() > 0) {
        // Output data in table format
        echo "<table>";
        echo "<tr><th>ID</th><th>Reg Number</th><th>Name</th><th>Institution ID</th><th>Created By</th><th>Created At</th><th>Updated At</th><th>Action</th></tr>";
        while ($row = $stmt->fetch(PDO::FETCH_ASSOC)) {
            echo "<tr>";
            echo "<td>" . $row["id"] . "</td>";
            echo "<td>" . $row["reg_no"] . "</td>";
            echo "<td>" . $row["name"] . "</td>";
            echo "<td>" . $row["institution_id"] . "</td>";
            echo "<td>" . $row["created_by"] . "</td>";
            echo "<td>" . $row["created_at"] . "</td>";
            echo "<td>" . $row["updated_at"] . "</td>";
            // Add buttons for Update and Enroll
            echo "<td>";
            echo "<a href='update_student.php?id={$row["id"]}' class='btn btn-primary'>Update</a>";
            echo "<a href='enroll_student.php?id={$row["id"]}' class='btn btn-success'>Enroll</a>";
            echo "</td>";
            echo "</tr>";
        }
        echo "</table>";
    } else {
        echo "0 results";
    }
} catch (PDOException $e) {
    echo "Error: " . $e->getMessage();
}
